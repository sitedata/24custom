				<h2><strong>VG WooCarousel - Product Carousel for WooCommerce</strong></h2>
				<p><strong>This plugin will help you to show more products of WooCommerce in beautiful responsive carousel slider. It has options for setting the pagination/sliding speed, enabling/disabling autoplay, showing bullet navigation and more.</strong></p> 

				<p><strong>Also, it is possible to define the number of items to be shown on desktop, tablet, mobile separately and <a href="http://codecanyon.net/item/vg-woocarousel-product-carousel-for-woocommerce/12931124" target="_blank">VG WooCarousel</a> will handle that automatically. It supports responsive design and works well on touch devices.</strong></p>

